public class unaryOperator {
    public static void main(String[] args) {
        int x = 10;
        // pre-increment
        System.out.println(++x);
        //post-increment
        System.out.print(x++);

        int y = 10;
        //pre-Decrement
        System.out.print(--y);
        //post-Decrement
        System.out.print(y--);

        //logical NOT
        boolean istrue = true;
        System.out.print(!isTrue);
    }
}
